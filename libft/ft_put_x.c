// Шестнадцатиричное целое число без знака

#include "libft.h"

int	ft_put_x(long long int x, char c)
{
	int	d;

	d = ft_len_x(x);
	if (x >= 16)
		ft_put_x(x / 16, c);
	if (c == 'x')
		ft_put_c("0123456789abcdef"[x % 16]);
	if (c == 'X')
		ft_put_c("0123456789ABCDEF"[x % 16]);
	return (d);
}
